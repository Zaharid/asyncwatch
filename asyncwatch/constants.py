class EVENTS(enum.IntEnum):
    ACCESS =0x00000001;
    MODIFY =0x00000002;
    ATTRIB =0x00000004;
    CLOSE_WRITE =0x00000008;
    CLOSE_NOWRITE =0x00000010;
    OPEN =0x00000020;
    MOVED_FROM =0x00000040;
    MOVED_TO =0x00000080;
    CREATE =0x00000100;
    DELETE =0x00000200;
    DELETE_SELF =0x00000400;
    MOVE_SELF =0x00000800;

    CLOSE = (CLOSE_WRITE | CLOSE_NOWRITE)
    MOVE = (MOVED_FROM | MOVED_TO)

    ALL_EVENTS = (ACCESS | MODIFY | ATTRIB | CLOSE_WRITE
                 | CLOSE_NOWRITE | OPEN | MOVED_FROM
                 | MOVED_TO | CREATE | DELETE
                 | DELETE_SELF | MOVE_SELF)

REAL_EVENTS = (
 EVENTS.ACCESS,
 EVENTS.MODIFY,
 EVENTS.ATTRIB,
 EVENTS.CLOSE_WRITE,
 EVENTS.CLOSE_NOWRITE,
 EVENTS.OPEN,
 EVENTS.MOVED_FROM,
 EVENTS.MOVED_TO,
 EVENTS.CREATE,
 EVENTS.DELETE,
 EVENTS.DELETE_SELF,
 EVENTS.MOVE_SELF,
)



class WATCH_FLAGS(enum.IntEnum):
    ONLYDIR = 0x01000000
    DONT_FOLLOW = 0x02000000
    MASK_ADD = 0x20000000
    ONESHOT = 0x80000000
    EXCL_UNLINK = 0x04000000

class RETURN_FLAGS(enum.IntEnum):
    UNMOUNT = 0x00002000
    Q_OVERFLOW = 0x00004000
    IGNORED = 0x00008000
    ISDIR = 0x40000000

EVENT_TYPES = REAL_EVENTS + (RETURN_FLAGS.UNMOUNT, RETURN_FLAGS.IGNORED)